using System;


public class Animal
{
	public string nome;
	public string raca;
	public int velocidade;
	public int stamina;
	public bool saude;
	public int dono;
	public int valor;
	public Animal(string nome, string raca, int velocidade, int stamina, bool saude, int dono, int valor)
	{

		this.nome = nome;
		this.raca = raca;
		this.velocidade = velocidade;
		this.stamina = stamina;
		this.saude = saude;
		this.dono = dono;
		this.valor = valor;
		

	}

}
